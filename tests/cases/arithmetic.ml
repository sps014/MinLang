fun main(): void {
    let a = 10;
    let b = 3;
    
    println(a + b);
    println(a - b);
    println(a * b);
    println(a / b);
    println(a % b);
    
    let x = 1.5;
    let y = 2.0;
    println(x + y);
    println(x * y);
    
    // Precedence
    println(1 + 2 * 3);
    println((1 + 2) * 3);
}
